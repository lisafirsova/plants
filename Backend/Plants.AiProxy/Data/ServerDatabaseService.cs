    private static string NormalizePlantType(string value)
    {
        var text = value.Trim();
        return text switch
        {
            "Кактусы" => "Суккуленты",
            "Кактусы и суккуленты" => "Суккуленты",
            "Суккулент" => "Суккуленты",
            "Листвен" => "Лиственные",
            _ => text
        };
    }

