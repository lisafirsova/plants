    public async Task<List<Pest>> GetPestsAsync(bool? isPest = null, string? plantType = null, string? query = null)
    {
        await ReadyAsync();
        var app_pests = new List<Pest>();
        await using var connection = await OpenAsync();
        var sql = """
            select i.id,
                   i.name,
                   i.treatment_description,
                   i.image_path,
                   c.code = 'pest' as is_pest,
                   coalesce(string_agg(distinct t.name, ', ' order by t.name) filter (where t.name is not null), '') as plant_types
            from app_reference_items i
            join app_reference_categories c on c.id = i.category_id
            left join app_reference_item_plant_types m_all on m_all.reference_item_id = i.id
            left join app_plant_types t on t.id = m_all.plant_type_id
            where (@category_code = '' or c.code = @category_code)
              and (@query = '' or i.name ilike @query_like or i.treatment_description ilike @query_like)
              and (
                    @plant_type = ''
                    or not exists (
                        select 1
                        from app_reference_item_plant_types m
                        where m.reference_item_id = i.id
                    )
                    or exists (
                        select 1
                        from app_reference_item_plant_types m
                        join app_plant_types t on t.id = m.plant_type_id
                        where m.reference_item_id = i.id and t.name = @plant_type
                    )
                  )
            group by i.id, i.name, i.treatment_description, i.image_path, c.code
            order by i.name
            """;
        await using var command = new NpgsqlCommand(sql, connection);
        command.Parameters.AddWithValue("category_code", isPest is null ? string.Empty : isPest.Value ? "pest" : "disease");
        command.Parameters.AddWithValue("plant_type", plantType ?? string.Empty);
        command.Parameters.AddWithValue("query", query?.Trim() ?? string.Empty);
        command.Parameters.AddWithValue("query_like", $"%{query?.Trim() ?? string.Empty}%");
        await using var reader = await command.ExecuteReaderAsync();
        while (await reader.ReadAsync())
        {
            app_pests.Add(ReadPest(reader));
        }
        return app_pests;
    }

    public Task<List<Pest>> GetPestsAsync() => GetPestsAsync(true);

    public Task<List<Pest>> GetDiseasesFromPestTableAsync() => GetPestsAsync(false);

    public Task<List<Pest>> GetAllPestRecordsAsync() => GetPestsAsync(null);

    public async Task<int> AddPestAsync(Pest pest)
    {
        await ReadyAsync();
        await using var connection = await OpenAsync();
        await using var transaction = await connection.BeginTransactionAsync();
        await using var command = new NpgsqlCommand("""
            insert into app_reference_items (category_id, name, treatment_description, image_path)
            values ((select id from app_reference_categories where code=@category_code), @name, @treatment_description, @image_path)
            returning id
            """, connection, transaction);
        FillPest(command, pest);
        pest.Id = Convert.ToInt32(await command.ExecuteScalarAsync());
        await SavePestPlantTypesAsync(connection, transaction, pest.Id, pest.PlantTypes);
        await transaction.CommitAsync();
        return pest.Id;
    }

    public async Task<int> UpdatePestAsync(Pest pest)
    {
        await ReadyAsync();
        await using var connection = await OpenAsync();
        await using var transaction = await connection.BeginTransactionAsync();
        await using var command = new NpgsqlCommand("""
            update app_reference_items
            set category_id=(select id from app_reference_categories where code=@category_code),
                name=@name,
                treatment_description=@treatment_description,
                image_path=@image_path
            where id=@id
            """, connection, transaction);
        command.Parameters.AddWithValue("id", pest.Id);
        FillPest(command, pest);
        var affected = await command.ExecuteNonQueryAsync();
        await SavePestPlantTypesAsync(connection, transaction, pest.Id, pest.PlantTypes);
        await transaction.CommitAsync();
        return affected;
    }

    public async Task<int> DeletePestAsync(int id)
    {
        await ReadyAsync();
        await using var connection = await OpenAsync();
        await using var command = new NpgsqlCommand("delete from app_reference_items where id=@id", connection);
        command.Parameters.AddWithValue("id", id);
        return await command.ExecuteNonQueryAsync();
    }

