    private static Pest ReadPest(NpgsqlDataReader reader) => new()
    {
        Id = reader.GetInt32(0),
        Name = reader.GetString(1),
        TreatmentDescription = reader.GetString(2),
        ImagePath = reader.GetString(3),
        IsPest = reader.GetBoolean(4)
        ,
        PlantTypes = reader.IsDBNull(5)
            ? []
            : reader.GetString(5)
                .Split(',', StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries)
                .ToList()
    };

    private static async Task SavePestPlantTypesAsync(NpgsqlConnection connection, NpgsqlTransaction transaction, int referenceItemId, IEnumerable<string> plantTypes)
    {
        await using (var delete = new NpgsqlCommand("delete from app_reference_item_plant_types where reference_item_id=@id", connection, transaction))
        {
            delete.Parameters.AddWithValue("id", referenceItemId);
            await delete.ExecuteNonQueryAsync();
        }

        var selected = plantTypes
            .Where(x => !string.IsNullOrWhiteSpace(x))
            .Select(NormalizePlantType)
            .Where(x => !string.IsNullOrWhiteSpace(x))
            .Distinct()
            .ToList();

        foreach (var plantType in selected)
        {
            await using var insert = new NpgsqlCommand("""
                insert into app_reference_item_plant_types (reference_item_id, plant_type_id)
                select @id, id
                from app_plant_types
                where name=@plant_type
                on conflict do nothing
                """, connection, transaction);
            insert.Parameters.AddWithValue("id", referenceItemId);
            insert.Parameters.AddWithValue("plant_type", plantType);
            await insert.ExecuteNonQueryAsync();
        }
    }

